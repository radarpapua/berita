      <h2>Latar Belakang Proyek</h2>
      <p>
        Pemerintah Provinsi Papua pada hari ini meresmikan pembangunan Jalan Trans-Papua seksi Jayapura–Sentani—
        proyek strategis yang menjadi bagian dari koridor utama penerima manfaat distribusi logistik wilayah Papua.
        Pekerjaan ini diharapkan dapat memperpendek waktu tempuh dari bandara Sentani ke pusat kota Jayapura,
        sekaligus mengurangi biaya transportasi dan membuka akses bagi daerah-daerah pedalaman.
      </p>

      <h2>Manfaat bagi Masyarakat</h2>
      <p>
        Gubernur Papua, Lukas Enembe, menyatakan bahwa kehadiran jalan baru ini akan meningkatkan akselerasi
        ekonomi lokal. “Selain memudahkan mobilitas warga, proyek ini juga membuka peluang usaha baru,
        khususnya di sektor pertanian dan pariwisata. Bahan pangan hasil petani di dataran tinggi bisa sampai
        ke pasar kota dengan lebih cepat,” ujarnya dalam sambutan peresmian.
      </p>

      <h2>Dukungan Pendanaan dan Mitra Kerja</h2>
      <p>
        Pendanaan proyek Rp1,2 triliun ini berasal dari APBN dan dukungan pinjaman lunak dari lembaga internasional.
        PT Wijaya Karya (WIKA) ditunjuk sebagai kontraktor pelaksana utama, bekerjasama dengan BUMN lokal
        serta sejumlah kontraktor karya daerah. Tahap awal meliputi pembangunan perkerasan jalan sejauh 25 kilometer,
        pengerasan bahu jalan, dan perbaikan jembatan kecil.
      </p>

      <h2>Tantangan dan Solusi Teknis</h2>
      <p>
        Salah satu tantangan terbesar adalah kondisi topografi yang curam dan rawan longsor, terutama pada musim hujan.
        Tim teknik WIKA menerapkan teknologi terowongan mikropile dan sistem drainase vertikal untuk meningkatkan
        stabilitas lereng. Selain itu, program penghijauan ditargetkan untuk menanam 10.000 pohon lokal di sepanjang
        koridor jalan.
      </p>

      <h2>Tanggapan Warga Setempat</h2>
      <p>
        Warga Kampung Harapan, Sentani, menyambut baik inisiatif pemerintah. Rita Wanggai, ketua RT setempat,
        mengatakan bahwa selama ini akses jalan rusak kerap memperlambat penanganan darurat kesehatan dan pasokan
        kebutuhan pokok. “Kami berharap pembangunan ini selesai tepat waktu dan berkelanjutan agar manfaat bisa
        terus dirasakan oleh generasi mendatang,” katanya.
      </p>

      <p>
        Proyek Jalan Trans-Papua seksi Jayapura–Sentani ditargetkan rampung pada akhir 2026, dengan harapan
        konektivitas antarwilayah di Papua semakin terintegrasi dan dapat mendorong pertumbuhan ekonomi yang
        merata ke seluruh penjuru provinsi.
      </p>
